const makefon = document.getElementById("makefon");
var ele = document.getElementsByClassName('font35');
makefon.addEventListener('click', function() {
if (ele[0].style.fontFamily == "AVFontimer") {
    for (var i = 0; i < ele.length; i++ ) {
        ele[i].style.fontFamily = 'ZingRust';
    }
} else {
    for (var i = 0; i < ele.length; i++ ) {
        ele[i].style.fontFamily = 'AVFontimer';
    }    
}
});

